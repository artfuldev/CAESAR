namespace CAESAR.Chess.Core
{
    /// <summary>
    ///     Represents one of the two sides of play in the game
    /// </summary>
    public enum Side : sbyte
    {
        /// <summary>
        ///     Represents the white side of play in the game
        /// </summary>
        White = 1,

        /// <summary>
        ///     Represents the black side of play in the game
        /// </summary>
        Black = -1
    }
}